package com.spoors.beans.workSpecs;

public class WorkCustomerFieldMapping {

	
	private long id;
	private long propertyId;
	private String name;
	private String propertyName;
	private String fieldUniqueId;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getPropertyId() {
		return propertyId;
	}
	public void setPropertyId(long propertyId) {
		this.propertyId = propertyId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	public String getFieldUniqueId() {
		return fieldUniqueId;
	}
	public void setFieldUniqueId(String fieldUniqueId) {
		this.fieldUniqueId = fieldUniqueId;
	}
	
	
	
	
}
